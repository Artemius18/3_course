
public class Car {
    private int year;
    private String color;
    private String model;

    void setYear(int year) {
        this.year = year;
    }

    int getYear() {
        return year;
    }

    void setColor(String color) {
        this.color = color;
    }

    String getColor() {
        return color;
    }

    void setModel(String model) {
        this.model = model;
    }

    String getModel() {
        return model;
    }
}


